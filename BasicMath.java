public class BasicMath {
    public static void main(String[] args){
        //add -> '+'
        int a = 5;
        int b = 9;

        System.out.println(a+b);

        //sub -> '-'

        int num1 = 9;
        int num2 = 7;
        System.out.println(num1 - num2);

        //div -> '/'
        int i = 8;
        int j = 4;
        System.out.println(i/j);

        //multi -> '*'
        int c = 6;
        int d = 9;
        System.out.println(c * d);

        //modulo -> '%'
        int g = 15;
        int k = 2;
        System.out.println(k%g);
    }
}
